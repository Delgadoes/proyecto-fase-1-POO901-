    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int id = Integer.parseInt(txtId.getText());
    String nombre = txtNombre.getText();
    String usuario = txtUsuario.getText();
    String contraseña = new String(txtContraseña.getPassword());
    String privilegio = (String) cmbPrivilegio.getSelectedItem();

    if (nombre.isEmpty() || usuario.isEmpty() || contraseña.isEmpty() || privilegio == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.",
                                                  "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
    } else {
        Connection conn = ConexionBD.getConnection();
        if (conn != null) {
            try {
                String sql = "UPDATE usuarios SET nombre = ?, usuario = ?, contraseña = ?, privilegio = ? WHERE id = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, nombre);
                stmt.setString(2, usuario);
                stmt.setString(3, contraseña);
                stmt.setString(4, privilegio);
                stmt.setInt(5, id);

                int filasActualizadas = stmt.executeUpdate();
                if (filasActualizadas > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Usuario modificado exitosamente.",
                                                              "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    this.dispose(); // Cierra la ventana después de guardar
                }
            } catch (SQLException e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al modificar el usuario: " + e.getMessage(),
                                                          "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

